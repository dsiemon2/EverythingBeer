export function getBeerById(id: string): Beer | undefined {
  return beers.find((beer) => beer.id === id);
}

export function getBeersByBrewery(breweryId: string): Beer[] {
  return beers.filter((beer) => beer.brewery_id === breweryId);
}

export function getBeersByStyle(styleId: string): Beer[] {
  return beers.filter((beer) => beer.style_id === styleId);
}

export function searchBeers(query: string): Beer[] {
  const q = query.toLowerCase();
  return beers.filter(
    (beer) =>
      beer.name.toLowerCase().includes(q) ||
      beer.brewery_name.toLowerCase().includes(q) ||
      beer.style_name.toLowerCase().includes(q)
  );
}

export function filterBeers(filters: {
  style?: string;
  country?: string;
  tag?: string;
  abv_min?: number;
  abv_max?: number;
  availability?: string;
  is_craft?: boolean;
}): Beer[] {
  return beers.filter((beer) => {
    if (filters.style && beer.style_id !== filters.style) return false;
    if (filters.country && beer.country_of_origin.toLowerCase() !== filters.country.toLowerCase()) return false;
    if (filters.tag && !beer.tags.includes(filters.tag)) return false;
    if (filters.abv_min && (!beer.abv || beer.abv < filters.abv_min)) return false;
    if (filters.abv_max && (!beer.abv || beer.abv > filters.abv_max)) return false;
    if (filters.availability && beer.availability !== filters.availability) return false;
    if (filters.is_craft !== undefined && beer.is_craft !== filters.is_craft) return false;
    return true;
  });
}

export function getCraftBeers(): Beer[] {
  return beers.filter((beer) => beer.is_craft);
}

export function getCommercialBeers(): Beer[] {
  return beers.filter((beer) => beer.is_commercial);
}

export function getTrendingBeers(): Beer[] {
  return beers.filter((beer) => beer.is_trending);
}
