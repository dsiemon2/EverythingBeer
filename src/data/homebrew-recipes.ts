// ─── Helper Functions ───────────────────────────────────────

export function getAllRecipes(): HomebrewRecipe[] {
  return homebrewRecipes.sort((a, b) => a.title.localeCompare(b.title));
}

export function getRecipeBySlug(slug: string): HomebrewRecipe | undefined {
  return homebrewRecipes.find((r) => r.slug === slug);
}

export function getRecipesByDifficulty(difficulty: HomebrewRecipe['difficulty']): HomebrewRecipe[] {
  return homebrewRecipes.filter((r) => r.difficulty === difficulty);
}

export function getRecipesByMethod(method: HomebrewRecipe['method']): HomebrewRecipe[] {
  return homebrewRecipes.filter((r) => r.method === method);
}

export function getRecipesByTag(tag: string): HomebrewRecipe[] {
  return homebrewRecipes.filter((r) => r.tags.includes(tag.toLowerCase()));
}
