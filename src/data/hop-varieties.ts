// ─── Helper Functions ───────────────────────────────────────

export function getAllHops(): HopVariety[] {
  return hopVarieties.sort((a, b) => a.name.localeCompare(b.name));
}

export function getHopByName(name: string): HopVariety | undefined {
  return hopVarieties.find((h) => h.name.toLowerCase() === name.toLowerCase());
}

export function getHopById(id: string): HopVariety | undefined {
  return hopVarieties.find((h) => h.id === id);
}

export function getHopsByPurpose(purpose: HopVariety['purpose']): HopVariety[] {
  return hopVarieties.filter((h) => h.purpose === purpose).sort((a, b) => a.name.localeCompare(b.name));
}

export function getHopsByOrigin(origin: string): HopVariety[] {
  return hopVarieties.filter((h) => h.origin.toLowerCase().includes(origin.toLowerCase())).sort((a, b) => a.name.localeCompare(b.name));
}

export function getUniqueOrigins(): string[] {
  return [...new Set(hopVarieties.map((h) => h.origin))].sort();
}

export function getUniqueAromas(): string[] {
  return [...new Set(hopVarieties.flatMap((h) => h.aromas))].sort();
}
